package symbols;

public class Symbols {

	public static void main(String[] args) {
		
		System.out.println("Symbols used in java programming");
		System.out.println("================================");
		
		System.out.println("My name is"+" Kunal");// + used to join two statements
		System.out.println("I'm from"+" Pune");
		System.out.println("-----------------");
		System.out.println("I have completed BCA in 2019"+"\n"+"from Pune University.");
		System.out.println("My Hobbies are"+"\n"+"Reading books and listening Music.");// /n used to move to a new line at this specific point
		System.out.println("There are seven family members in my family including me. \nThats all about of me sir.");
		
	}

}
