package castingOR_Conversion;

public class Explicit_Casting {

	public static void main(String[] args)
	
	{  
		// Conversion of Higher data info. into lower data info.
		int i=100000;
		short b=(short)i;
		System.out.println(b);
		
		float a=4545445454f;
		short t=(short)a;
		System.out.println(t);
		
		double d=81893d;
		float s=(float)d;
		System.out.println(s);
		
		
		
		
		
	}

}
