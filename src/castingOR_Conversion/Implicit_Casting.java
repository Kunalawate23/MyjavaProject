package castingOR_Conversion;

public class Implicit_Casting {
	
	public static void main(String[] args) 
	{  
	 // Conversion of lower data info. into higher data info
	   byte a=10;
	   short h=a;
	   System.out.println(h);
	   
	   short b=100;
	   int c=b;
	   System.out.println(c);
	   
	   int e=1010;
	   long l=e;
	   System.out.println(l);
		

	}

}
