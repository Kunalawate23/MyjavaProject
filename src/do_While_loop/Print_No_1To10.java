package do_While_loop;

public class Print_No_1To10 {

	public static void main(String[] args) {
		
		System.out.println("print No. 1 to 10 using do while loop");
		System.out.println("========================================");
		
		int x=1;
		do
		{
			System.out.println(x);
			x++;
		}
		   while(x<=10);
			   
	


	}

}
