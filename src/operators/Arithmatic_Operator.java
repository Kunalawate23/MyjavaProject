package operators;

public class Arithmatic_Operator {

	public static void main(String[] args) {
		int a=20;
		int b=8;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);//Answer is quotient
		System.out.println(a%b);//Answer is reminder
		
	}

}
