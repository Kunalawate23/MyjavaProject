package operators;

public class Unary_Operator {

	public static void main(String[] args) 
	{// Postfix increment
		int a=12;
		System.out.println(a);
		System.out.println(a++);
		System.out.println(a);
		System.out.println(a++);
		System.out.println(a);
		
	System.out.println("=======================");
	
	// Postfix decrement
		int b=13;
		System.out.println(b);
		System.out.println(b--);
		System.out.println(b);
		System.out.println(b--);
		System.out.println(b);
		
    System.out.println("----------------------------------------");
   
    //Prefix increment
         int c=14;
     	System.out.println(c);
    	System.out.println(++c);
    	System.out.println(c);
    	System.out.println(++c);
    	System.out.println(c);
    	
    System.out.println("=========================");
    
    //Prefix decrement
     int d=15;
 	  System.out.println(d);
	  System.out.println(--d);
	  System.out.println(d);
	  System.out.println(--d);
	  System.out.println(d);
    	
		
	}

}
