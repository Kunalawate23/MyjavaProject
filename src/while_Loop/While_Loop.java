package while_Loop;

public class While_Loop {

	public static void main(String[] args) {
	      System.out.println("Print 10 to 1 numbers using while loop");
	      System.out.println("======================================");
	      
	      
	      int i=10;
	      
	      while(i>=1) 
	      
	      {
	    	  System.out.println(i);
	    	  i--;
	    	  
	      }
	    	  
	    		  

	}

}
