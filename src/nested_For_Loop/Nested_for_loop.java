package nested_For_Loop;

public class Nested_for_loop {

	public static void main(String[] args) {
		
		System.out.println("print 1 2 3");
		System.out.println("===========");
		
		for(int i=1;i<=3;i++)
		{
			for(int j=1;j<=3;j++)
			{
				System.out.println(i+" "+j);
				
			}
		}
	}

}
